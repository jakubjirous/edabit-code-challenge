/**
 * Return the Sum of Two Numbers
 *
 * Create a function that takes two numbers as arguments and returns their sum.
 *
 * Example:
 * - addition(3, 2) ➞ 5
 * - addition(-3, -6) ➞ -9
 * - addition(7, 3) ➞ 10
 */
export const addition = (param1: number, param2: number): number => {
  return param1 + param2;
};

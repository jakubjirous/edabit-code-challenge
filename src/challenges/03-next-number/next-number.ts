/**
 * Return the Next Number from the Integer Passed
 *
 * Create a function that takes a number as an argument, increments the number by +1 and returns the result.
 *
 * Example:
 * - nextNumber(0) ➞ 1
 * - nextNumber(9) ➞ 10
 * - nextNumber(-3) ➞ -2
 */
export const nextNumber = (input: number): number => {
  return input + 1;
};
